# bi_data_analysis_answers.py

# 1. Why do you think companies analyze large volumes of data?
# Companies analyze large volumes of data to understand customer behavior,
# improve decision-making, identify trends, reduce costs, and increase profits.
# Data helps companies make decisions based on facts rather than assumptions
# and gives them a competitive advantage in the market.

# 2. If analyzing and sorting large data manually in Excel is difficult,
# how do you think Python can help solve this problem?
# Python can automate data analysis tasks, handle very large datasets efficiently,
# and perform complex calculations much faster than manual Excel work.
# Libraries like pandas allow easy sorting, filtering, and transforming of data
# with just a few lines of code.

# 3. Imagine you work at a sales company that receives data about 10,000
# customer transactions daily. How would you analyze this data?
# I would use Python to load the daily data, clean it, and store it in a database.
# Then I would analyze sales trends, total revenue, popular products,
# and customer purchasing behavior using Python libraries such as pandas and numpy.
# Visualizations could be created to summarize the results.

# 4. In your opinion, what tasks can Python be useful for in BI processes?
# Python is useful for many BI tasks such as data cleaning, data transformation,
# statistical analysis, data visualization, reporting, automation, and
# integrating data from multiple sources.

# 5. If you wanted to compare a company's profit year by year,
# how could this be done using Python?
# Using Python, I could load profit data for each year into a pandas DataFrame,
# group the data by year, calculate yearly profits, and then compare them.
# Charts such as line graphs or bar charts could be created to visualize
# profit changes over time.

# 6. If you don't know Python, what difficulties might you face
# when working with large datasets?
# Without Python, working with large datasets can be slow and inefficient.
# Manual analysis increases the risk of errors, automation is difficult,
# and handling very large or complex datasets may not be possible using
# basic tools like Excel alone.
